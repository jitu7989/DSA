const int N = 1e5*2+3;
ll n,x;

vector<ll> arr(N);
set<ll> s;
multiset<ll> ms;

void solve(){
    cin >> n ;
    s.clear();
    ms.clear();
    for (int i = 0; i < n; ++i) {
        cin >> arr[i];
        if(arr[i]<=n && s.find(arr[i])==s.end()) {
            s.insert(arr[i]);
        }
        else {
            ms.insert(arr[i]);
        }
    }

    int ops = 0;

    for (int i = 1; i<=n; ++i) {
        if(s.find(i)==s.end()){

            if( ((2*i)+1) > *ms.begin()){
                cout << "-1\n";
               return; 
            };
            ops++;
            ms.erase(ms.begin());
        }
    }
    cout << ops << '\n';
}

signed main(){
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);
    
    int t; cin >> t;
    while(t--) solve();
    return 0;
}