void solve(){
    int n,b,a; cin >> n >> b >> a;

    int total = n-2;
    int max = ceil(total/2.0);

    if(abs(a-b)>1 || a+b>total){
        cout << -1 << '\n';
        return;
    }

    bool movel = a<=b;
    int l = 1,r=n;
    while(l<=r){
        if(movel){ 
            cout << l << ' ';
            if(a && ((r-l)+1)!=n ) a--;
            l++;
        }
        else {
            cout << r << ' ';
            if(b && ((r-l)+1)!=n) b--;
            r--;
        }
        if(a>0 || b>0) movel=!movel;
    }
    cout << '\n';

}

